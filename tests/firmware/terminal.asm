UPDATE_CURSOR:
    MOV32 [0x10000], [CURSOR_X]
    MOV32 [0x10004], [CURSOR_Y]
    RET

PUT_CHAR:
    ; X = G1
    ; Y = G2
    ; CH = G3
    MOV64 G4, 0
    ; Y*WIDTH+X
    MOV32 G4, G2
    MUL32 G4, [0x10008]
    ADD32 G4, G1
    MUL32 G4, 2
    ADD32 G4, 0x10100
    OR16 G3, 0x0200
    MOV16 [G4], G3
    RET

NEWLINE:
    ; TODO: Check for scroll
    MOV32 [CURSOR_X], 0
    ADD32 [CURSOR_Y], 1
    CALL UPDATE_CURSOR
    RET

PRINT:
    ; STR = G0
    PUSH64 G1
    PUSH64 G2
    PUSH64 G3
PRINT_LOOP:
    CMP8 [G0], 0
    JE PRINT_EXIT
    CMP8 [G0], 10
    JE PRINT_NL
    MOV32 G1, [CURSOR_X]
    MOV32 G2, [CURSOR_Y]
    MOV8 G3, [G0]
    CALL PUT_CHAR
    ADD32 [CURSOR_X], 1
    CALL UPDATE_CURSOR
    ADD64 G0, 1
    JMP PRINT_LOOP
PRINT_NL:
    CALL NEWLINE
    ADD64 G0, 1
    JMP PRINT_LOOP
PRINT_EXIT:
    POP64 G3
    POP64 G2
    POP64 G1
    RET

CURSOR_X: D32 0
CURSOR_Y: D32 0